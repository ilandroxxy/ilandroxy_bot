def Prime(x):
    if x <= 1:
        return False
    for j in range(2, int(x ** 0.5) + 1):
        if x % j == 0:
            return False
    return True


def divisors(x):
    div = []
    for j in range(1, int(x ** 0.5) + 1):
        if x % j == 0:
            if Prime(j) and Prime(x // j):
                div.append(j)
                div.append(x // j)
    return sorted(set(div))


for x in range(125697, 125721+1):
    d = divisors(x)
    if len(d) > 0:
        print(*d)
