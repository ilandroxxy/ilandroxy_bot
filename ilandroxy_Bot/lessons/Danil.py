def prime(x):
    if x == 1:
        return False
    for j in range(2, int(x ** 0.5) + 1):
        if x % j == 0:
            return False
    return True


def divisors(x):
    div = []
    for j in range(1, int(x**0.5)+1):
        if x % j == 0:
            if prime(j) and prime(x // j):
                div += [j, x // j]
    return sorted(set(div))


