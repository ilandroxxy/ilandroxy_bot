# Тип данных коллекций
'''
A = [1, 2, 3, 2, 3]  # list (список)
# В списке можно хранить неограниченное кол-во элементов
# Каждый элемент списка имеет порядковый номер - индекс
# Индексы начинают свой счет с нуля
# Так же можно пробегать список в обратном порядке через отрицательные индексы
print(A[0], A[-1])  # То есть, индекс [0] это первый элемент, а [-1] это последний
# Через индексы мы можем брать элементы списка И ИЗМЕНЯТЬ ИХ
A[0] = 99999
print(A)
print(*A)  # раскрыть список и вывести в виде строки

B = (1, 2, 3, 2, 3)  # tuple (кортеж)
# Кортеж полностью идентичен списку, но ИЗМЕНЯТЬ ЭЛЕМЕНТЫ НЕЛЬЗЯ

C = {1, 2, 3, 2, 3}  # set (множества)
# Элементы множества не имеют точного порядка, постоянно перемешиваются
# В множестве не может быть двух одинаковых элементов, то есть удаляются копии
print(set(A))

D = {1: 'one', 2: 'two', 3: 'three'}  # dict (словарь)
# доступ к значению элемента словаря осуществляется по его ключу
print(D.keys())
print(D.values())
print(D.items())
'''


