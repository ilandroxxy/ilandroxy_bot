x = float(x)
print(x, type(x))  # можно спокойно конвертировать

x = int(x)
print(x, type(x))  # можно спокойно конвертировать

M = [1, 2, 3]
print(M, type(M))

M = set(M)
print(M, type(M))

M = tuple(M)
print(M, type(M))

M = list(M)
print(M, type(M))
