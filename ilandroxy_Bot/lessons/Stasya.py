# –æ–∫–∞–Ω—á–∏–≤–∞—é—â–∏—Ö—Å—è –Ω–∞ 31. –í –æ—Ç–≤–µ—Ç–µ –∑–∞–ø–∏—à–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –ø–∞—Ä, –∑–∞—Ç–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –∏–∑ —Å—É–º–º —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Ç–∞–∫–∏—Ö –ø–∞—Ä.
# –í –¥–∞–Ω–Ω–æ–π –∑–∞–¥–∞—á–µ –ø–æ–¥ –ø–∞—Ä–æ–π –ø–æ–¥—Ä–∞–∑—É–º–µ–≤–∞–µ—Ç—Å—è –¥–≤–∞ –∏–¥—É—â–∏—Ö –ø–æ–¥—Ä—è–¥ —ç–ª–µ–º–µ–Ω—Ç–∞ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏.

M = [int(i) for i in open('17.txt')]
A = [i for i in M if abs(i) % 100 == 31]
R = abs(min(A) + max(A))  # –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è —Å—É–º–º—ã –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –∏ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–≥–æ
count = 0
maxi = 0
for i in range(0, len(M)-1):
    if M[i] > R and M[i+1] > R:
        count += 1
        maxi = max(maxi, M[i] + M[i+1])
print(count, maxi)

