# –°–∫–æ–ª—å–∫–æ –≤ —ç—Ç–æ–π —Å–µ—Ç–∏ IP-–∞–¥—Ä–µ—Å–æ–≤, –¥–ª—è –∫–æ—Ç–æ—Ä—ã—Ö —Å—É–º–º–∞ –µ–¥–∏–Ω–∏—Ü –≤ –¥–≤–æ–∏—á–Ω–æ–π –∑–∞–ø–∏—Å–∏ —á–µ—Ç–≤—ë—Ä—Ç–æ–≥–æ –±–∞–π—Ç–∞ IP-–∞–¥—Ä–µ—Å–∞
