# Тип 8 №16439
# Михаил составляет 6-буквенные коды. В кодах разрешается
# использовать только буквы А, Б, В, Г, при этом код не может
# начинаться с гласной и не может содержать двух одинаковых букв подряд.
# Сколько различных кодов может составить Михаил?
'''
from itertools import *
cnt = 0
for s in product('АБВГ', repeat=6):
    slovo = ''.join(s)
    if slovo[0] not in 'А':
        if all(pair not in slovo for pair in 'АА ББ ВВ ГГ'.split()):
            cnt += 1
print(cnt)
'''


# Тип 8 №9194
