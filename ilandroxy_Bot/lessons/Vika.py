# Тип 8 №59742
# Определите количество четырехзначных чисел, записанных
# в десятичной системе счисления, в записи которых все
# цифры различны и никакие
# две чётные и две нечётные цифры не стоят рядом.
'''
from itertools import *
cnt = 0
for s in permutations('0123456789', 4):
    slovo = ''.join(s)
    if slovo[0] != '0':
        slovo = slovo.replace('0', '2').replace('4', '2').replace('6', '2').replace('8', '2')
        slovo = slovo.replace('3', '1').replace('5', '1').replace('7', '1').replace('9', '1')
        if '22' not in slovo and '11' not in slovo:
            cnt += 1
print(cnt)
'''


