# полученного на предыдущем шаге.
#
# При каком наименьшем N в результате работы алгоритма получится R = 979?
'''
for n in range(1, 10000):
    s = bin(n)[2:]
    s = s.replace('1', '*').replace('0', '1').replace('*', '0')
    r = int(s, 2)
    if n - r == 979:
        print(n)
        break
'''
