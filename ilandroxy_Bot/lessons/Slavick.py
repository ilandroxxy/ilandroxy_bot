#
# 1.  Строится двоичная запись числа N.
# 2.  Далее эта запись обрабатывается по следующему правилу:
# а)  если число N делится на 3, то в этой записи дописываются справа
# три последние двоичные цифры;
# 6)  если число N на 3 не делится, то остаток от деления умножается на 3,
# переводится в двоичную запись и дописывается в конец числа.
#
# 3.  Результат переводится в десятичную систему и выводится на экран.

# Укажите минимальное число N, после обработки которого с помощью этого алгоритма
# получается число R, не меньшее чем 76.
'''
for n in range(1, 10000):
    s = bin(n)[2:]
    if n % 3 == 0:
        s = s + s[-3:]
    else:
        x = (n % 3) * 3
        s = s + bin(x)[2:]
    r = int(s, 2)
    if r >= 76:
        print(n)
        break
'''
# Ответ: 11


